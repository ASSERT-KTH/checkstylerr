package com.arm.pelion.sdk.foundation.generator.util;

public class FoundationGeneratorException extends Exception {

    /**
     *
     */
    private static final long serialVersionUID = -8956092635718075159L;

    public FoundationGeneratorException() {
        super();
    }

    public FoundationGeneratorException(String arg0) {
        super(arg0);
    }

    public FoundationGeneratorException(Throwable arg0) {
        super(arg0);
    }

    public FoundationGeneratorException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public FoundationGeneratorException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }

}

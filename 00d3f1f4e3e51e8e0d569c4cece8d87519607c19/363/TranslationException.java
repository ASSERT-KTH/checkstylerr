package com.arm.pelion.sdk.foundation.generator.util;

public class TranslationException extends FoundationGeneratorException {

    /**
     *
     */
    private static final long serialVersionUID = -6797277083630881032L;

    public TranslationException() {
        super();
    }

    public TranslationException(String arg0) {
        super(arg0);
    }

    public TranslationException(Throwable arg0) {
        super(arg0);
    }

    public TranslationException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public TranslationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }

}
